def encoder(input_str):
    """
    Преобразует строку с числами, разделёнными запятыми, в список целых чисел.

    :param input_str: Строка с числами, разделёнными запятыми (например, '1,2,4,6,7')
    :return: Список целых чисел (например, [1, 2, 4, 6, 7])
    """
    # Разделяем строку по запятым и преобразуем каждый элемент в целое число
    number_list = [int(num) for num in input_str.split(',')]
    return number_list


def decoder(input_list):
    """
    Преобразует список целых чисел обратно в строку с числами, разделёнными запятыми.

    :param input_list: Список целых чисел (например, [1, 2, 4, 6, 7])
    :return: Строка с числами, разделёнными запятыми (например, '1,2,4,6,7')
    """
    # Преобразуем каждый элемент списка в строку и объединяем их через запятую
    output_str = ','.join(map(str, input_list))
    return output_str
